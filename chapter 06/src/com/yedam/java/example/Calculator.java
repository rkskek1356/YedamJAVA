package com.yedam.java.example;

public class Calculator {
	
	int plus(int x, int y) {
		int result = 0;
		result = x + y;
		return result;
	}
	
	double division(int x, int y) {
		double result = 0;
		result = (double)x / y;
		return result;
	}
	
	int minus(int x, int y) {
		int result = 0;
		result = x - y;
		return result;
	}
	
	int multi(int x, int y) {
		int result = 0;
		result = x * y;
		return result;
	}

}
